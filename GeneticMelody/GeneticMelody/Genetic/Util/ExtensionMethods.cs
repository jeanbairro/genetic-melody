using System.Collections.Generic;

namespace GeneticMelody.Genetic.Util
{
    public static class ExtensionMethods
    {
        public static IList<T> Swap<T>(this IList<T> list, int indexA, int indexB)
        {
            T tmp = list[indexA];
            list[indexA] = list[indexB];
            list[indexB] = tmp;
            return list;
        }
    }
}